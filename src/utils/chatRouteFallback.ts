const MESSAGE_IRRELEVANT = "Не удалось распознать рецепт. Попробуйте уточнить запрос. Этот чат помогает подбирать рецепты. Напишите, что хотите приготовить, и я предложу подходящий рецепт.";

function normalize(text: string): string {
  return (text ?? "").trim().toLowerCase();
}

/**
 * Если запрос явно про тему Помощника или нерелевантен — возвращает текст сообщения для чата.
 * Иначе null (показывать ответ бэкенда или FAILED_MESSAGE).
 */
export function getRedirectOrIrrelevantMessage(userMessage: string): string | null {
  const lower = normalize(userMessage);
  if (lower.length < 2) return null;

  for (const { keywords, topicTitle } of ASSISTANT_TOPIC_KEYWORDS) {
    if (keywords.some((kw) => lower.includes(kw))) {
      return `Этот вопрос лучше задать во вкладке «Помощник», в теме «${topicTitle}». Там можно получить более точный ответ.`;
    }
  }

  if (IRRELEVANT_KEYWORDS.some((kw) => lower.includes(kw))) {
    return MESSAGE_IRRELEVANT;
  }

  return null;
}
