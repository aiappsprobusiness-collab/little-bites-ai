/**
 * Определяет эмодзи для ингредиента на основе его названия
 * @param ingredientName - название ингредиента
 * @returns эмодзи для ингредиента или дефолтный эмодзи
 */
export function getIngredientEmoji(ingredientName: string): string {
  if (!ingredientName) return '🥄';

  const name = ingredientName.toLowerCase().trim();

  // Ищем точное совпадение или частичное совпадение
  for (const [key, emoji] of Object.entries(ingredientEmojis)) {
    if (name.includes(key)) {
      return emoji;
    }
  }

  // Дефолтный эмодзи для неизвестных ингредиентов
  return '🥄';
}

/**
 * Получить все доступные эмодзи
 */
export function getAllEmojis(): IngredientEmojiMap {
  return { ...ingredientEmojis };
}
