/**
 * Текст подсказки в шапке чата рецептов (Free): без «X из Y» и без технич. формулировок.
 */
export function getRemainingRecipesText(remainingAttempts: number): string {
  const n = Math.floor(remainingAttempts);
  if (n <= 0) {
    return "Сегодня лимит подборов исчерпан";
  }
  if (n === 1) {
    return "Сегодня можно подобрать еще 1 рецепт";
  }
  if (n === 2) {
    return "Сегодня можно подобрать еще 2 рецепта";
  }
  /* Редкий случай, если дневной лимит в правилах > 2: без склонения по правилам 1/2/5 */
  return "Сегодня можно подобрать ещё несколько рецептов";
}
