export function buildEmergencyChatRecipeDescription(options: {
  title: string;
  ingredients?: string[];
  recipeIdSeed?: string;
  mealType?: string;
}): string {
  const title = (options.title ?? "").trim() || "Блюдо";
  const ingNames = (options.ingredients ?? []).map((i) => i.trim()).filter(Boolean);
  const seed = (options.recipeIdSeed ?? title + (ingNames[0] ?? "")).trim() || "default";
  const dishType = detectDescriptionDishType(title, ingNames, options.mealType);
  const pairs = EMERGENCY_PAIRS[dishType] ?? EMERGENCY_PAIRS.default;
  const pair = pairs[simpleHash(seed) % pairs.length] ?? pairs[0]!;
  const out = `${pair[0]} ${pair[1]}`.trim();
  return normalizeSpaces(out).slice(0, DESCRIPTION_MAX_LENGTH);
}
